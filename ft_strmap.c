#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char *new;
	unsigned int i;

	if (!s || !f)
		return (NULL);
	new = ft_strnew(ft_strlen(s));
	if (!new)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		new[i] = f(s[i]);
		i++;
	}
	return (new);
}
